    static func sparkForDate(_ date: Date) -> DateSparkIdea {
        let dayOfYear = Calendar.current.ordinality(of: .day, in: .year, for: date) ?? 1
        let currentSeason = Season.current(for: date)
        let seasonalSparks = sparks.filter { $0.season == .any || $0.season == currentSeason }
        let index = (dayOfYear - 1) % seasonalSparks.count
        return seasonalSparks[index]
    }

    static func indexForDate(_ date: Date) -> Int {
        let dayOfYear = Calendar.current.ordinality(of: .day, in: .year, for: date) ?? 1
        return (dayOfYear - 1) % sparks.count
    }
}

nonisolated struct DateSparkIdea: Codable, Sendable {
    let title: String
    let description: String
    let emoji: String
    let season: Season
}

nonisolated enum Season: String, Codable, Sendable {
    case spring, summer, autumn, winter, any

    static func current(for date: Date = Date()) -> Season {
        let month = Calendar.current.component(.month, from: date)
        switch month {
        case 3...5: return .spring
        case 6...8: return .summer
        case 9...11: return .autumn
        default: return .winter
        }
    }
}
